export function getCapability(industrySlug, capabilitySlug) {
  const industry = industryCapabilities[industrySlug]
  if (!industry) return null
  return industry[capabilitySlug] || null
}

export function getAllCapabilities() {
  const all = []
  for (const [industrySlug, capabilities] of Object.entries(industryCapabilities)) {
    for (const [capabilitySlug, data] of Object.entries(capabilities)) {
      all.push({ industrySlug, capabilitySlug, ...data })
    }
  }
  return all
}

export function getRelatedCapabilities(industrySlug, capabilitySlug) {
  const capability = getCapability(industrySlug, capabilitySlug)
  if (!capability) return []
  return capability.relatedCapabilities.map(slug => ({
    slug,
    data: getCapability(industrySlug, slug),
  })).filter(item => item.data !== null)
}
